public class java {
    public static void main(String[] args) {
        //contoh type Casting
         /* tipe casting ini adalah untuk mengubah tipe data satu ke tipe data lainnya*/ 
        int a = 12;
        byte b = (byte) a;
        short c = 13;
        long d = c;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
      
        
    }
    
}
